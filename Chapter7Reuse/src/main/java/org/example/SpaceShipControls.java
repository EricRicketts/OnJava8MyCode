package org.example;

public class SpaceShipControls {
    public String up(int velocity) {
        return "up by " + velocity + "ft/sec";
    }
    public String down(int velocity) {
        return "down by " + velocity + "ft/sec";
    }
    public String left(int velocity) {
        return " left by " + velocity + "ft/sec";
    }
    public String right(int velocity) {
        return "right by " + velocity + "ft/sec";
    }
    public String forward(int velocity) {
        return "forward by " + velocity + "ft/sec";
    }
    public String back(int velocity) {
        return "back by " + velocity + "ft/sec";
    }
}
